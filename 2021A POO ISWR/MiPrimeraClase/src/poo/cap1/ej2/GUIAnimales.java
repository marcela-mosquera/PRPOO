    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (contador < animales.length) {
            if (rbtConejo.isSelected()) {
                animales[contador] = new Conejo();
            }
            if (rbtGato.isSelected()) {
                animales[contador] = new Gato();
            }
            if (rbtnPerro.isSelected()) {
                animales[contador] = new Perro();
            }
            btgAnimales.clearSelection();
            JOptionPane.showMessageDialog(rootPane, "Animal agregado exitosamente");
            contador++;
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnVerAnimalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerAnimalesActionPerformed
        // TODO add your handling code here:

        /*
        El Ã­ndice que ocupa el animal en el arreglo, el tipo de animal al que corresponde.
        El alimento favorito en el caso de perros y gatos: croquetas, en el caso de conejos: alfalfa
        El sonido que emite si es un perro (guau guau guau) si es un gato (miau miau miau)
         */
        for (int i = 0; i < animales.length; i++) {
            txaInformacion.setText(txaInformacion.getText()
                    + "En el indice " + i + " se guardo un " + animales[i].getClass().getSimpleName()
                    + " alimento favorito: " + animales[i].mostrarAlimentoFavorito());
            if (animales[i] instanceof Mascota) {
                txaInformacion.setText(txaInformacion.getText()
                        + ((Mascota) animales[i]).emitirSonido()+"\n");
            }
        }


    }//GEN-LAST:event_btnVerAnimalesActionPerformed

