    private void brnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            char sexo = '\0';
            if (rbtHembra.isSelected()) {
                sexo = rbtHembra.getText().charAt(0);
                sexo = 'H';
            }
            if (rbtMacho.isSelected()) {
                sexo = 'M';
            }
            if (cmbTipoMascota.getSelectedIndex() == 1) {
                p1 = new Perro(txtRaza.getText(), txtNombre.getText(), sexo);
                txaInformacion.setText(p1.toString());
                esPerro = true;

            }
            if (cmbTipoMascota.getSelectedIndex() == 0) {
                g1 = new Gato(txtNombre.getText(), sexo);
                txaInformacion.setText(g1.nombre + " " + g1.getSexo());
                esGato = true;

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getStackTrace());
        }
    }//GEN-LAST:event_brnGuardarActionPerformed

