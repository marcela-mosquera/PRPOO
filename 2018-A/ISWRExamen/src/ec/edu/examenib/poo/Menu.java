    private void btnRegistrarTourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarTourActionPerformed
        // TODO add your handling code here:
        //condiciones para que el registro del tour suceda
        try {
            if (Integer.parseInt(txtNumeroPersonas.getText()) > 30) {
                JOptionPane.showMessageDialog(rootPane, "Número de personas no puede ser mayor a 30", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                //proceder a registrar el tour
                //tomar los datos de la GUI 

                String servicios = "";
                if (chkAlmuerzo.isSelected()) {
                    servicios += "Almuerzo, ";
                }
                if (chkDesayuno.isSelected()) {
                    servicios += "Desayuno, ";
                }
                if (chkGuiaTurismo.isSelected()) {
                    servicios += "Guia de turismo, ";
                }
                if (chkRecuerdo.isSelected()) {
                    servicios += "Recuerdos";
                }

                String ser[] = servicios.split("\\,");

                //creacion del tour
                Tour t = new Tour(cmbTipoTour.getSelectedItem().toString(),
                        Integer.parseInt(txtNumeroPersonas.getText()),
                        ser);
                System.out.println(t.getServicios());
                //asignar el cliente
                t.setCliente(c1);
                //asignar el asesor
                t.setAsesor(a1);

                //asignar transporte
                t.asignarTransporte();

                //calcular el valor a pagar
                t.calcularValorApagar();
                
                //asignar un seguro
                char tipo;
                double porcentajeDeCobertura;
                
                if(t.getValorAPagar() > 600)
                    porcentajeDeCobertura = 0.35; //0.35 int 
                else
                    porcentajeDeCobertura = 0.15; 
                
                if (t.getNumeroPersonas() > 20)
                    tipo='A';
                else
                    tipo='B';
                
                t.setSeguro(new Seguro(12,(t.getValorAPagar()*porcentajeDeCobertura),
                tipo,porcentajeDeCobertura)); //crear la parte que le pertenece al contenedor
                
                

                //presentar datos 
                mostrarTour(t);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo registrar el tour", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnRegistrarTourActionPerformed

