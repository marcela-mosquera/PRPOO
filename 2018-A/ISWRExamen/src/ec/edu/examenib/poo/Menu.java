    private void mostrarTour(Tour t) {
        dtmModelo.addRow(new Object[]{
            t.getAsesor().getNombre(),
            t.getCliente().toString(), //t.getCliente().getApellido().toUpperCase()+","+t.getCliente().getNombre().toUpperCase(),
            t.getCliente().getNumeroContacto(),
            t.getTipoDeTour(),
            t.getNumeroPersonas(),
            t.getServicios(),
            t.getTipoTransporte(),
            t.getValorAPagar(),
            t.getSeguro().getIdentificador(),
            t.getSeguro().getTipoSeguro(),
            t.getSeguro().getMontoMaximo()
        }
        );

    }
